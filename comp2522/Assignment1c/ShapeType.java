/**
 * COMP2522 LabA1c. 
 * The ShapeType enum represents the different types of geometric shapes that can be created.
 * Each enum value corresponds to a specific shape type.
 * @author Mun Young Cho (A01330048)
 */
public enum ShapeType {
    /**
     * Represents a rectangular shape.
     */
    RECTANGLE,

    /**
     * Represents a triangular shape.
     */
    TRIANGLE,

    /**
     * Represents a diamond shape.
     */
    DIAMOND
}
